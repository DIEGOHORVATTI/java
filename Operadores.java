/*
  +	  Adição        Adiciona dois valores................. x + y	
  -	  Subtração	    Subtrai um valor de outro............. x - y	
  *	  Multiplicação	Multiplica dois valores............... x * y	
  /	  Divisão	      Divide um valor por outro............. x / y	
  %   Módulo	      Retorna o restante da divisão......... x% y	
  ++	Incremento    Aumenta o valor de uma variável em 1.. ++ x	
  -	  Decremento    Diminui o valor de uma variável em 1.. --x
*/

/*
  Operadores de atribuição Java
  =   x = 5	  x= 5
  +=  x += 3	x= x+3	
  -=  x -= 3	x= x-3	
  *=  x *= 3	x= x*3	
  /=  x /= 3	x= x/3	
  %=  x %= 3	x= x%3	
  &=  x &= 3	x= x&3	
  |=  x |= 3	x= x|3	
  ^=  x ^= 3	x= x^3	
  >>= x >>= 3	x= x>3	
  <<= x <<= 3	x= x<3
*/

/*
  Operadores de comparação java
  ==	Igual a........... x == y	
  =!	Não é igual....... x != y	
  >	  Maior que......... x > y	
  <  	Menos que......... x < y	
  >=	Maior ou igual a.. x >= y	
  <=	Menos ou igual a.. x <= y
*/

/*
  Operadores lógicos java
  &&  Lógico e	Retorna true se ambas as afirmações forem verdadeiras... x < 5 &&  x < 10	
  ||  Lógico ou	Retorna true se uma das afirmações for verdadeira....... x < 5 || x < 4	
  !   Retorna false se o resultado for verdadeiro....................... !(x < 5 && x < 10)
*/